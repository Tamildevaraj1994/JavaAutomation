package javapackage;

public class ifelseladder {

	public static void main(String[] args) {
		// If else ladder demo
		
		int x=200;
		int y=200;
		
		if (x < y) {
			System.out.println("X is greater");
		} 
		else if(x==y) {
	System.out.println("X is equal to y");
}
		else {
			System.out.println("X is smaller");
		}

	}

}
