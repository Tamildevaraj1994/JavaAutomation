package javapackage;

public class Logicaloperator {

	public static void main(String[] args) {
		// Logicaloperator Demo
		
		int a=100;//local variable
		int b=50;
		int c=200;
		
		//Logical operator
		
		System.out.println(a<b && a<c);//false
		
		//Bitwise operator
		
		System.out.println(a<b & a<c);//false

	}

}
