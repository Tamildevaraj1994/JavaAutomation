package javapackage;

public class Relationaloperator {

	public static void main(String[] args) {
		// Relationaloperatordemo
		
		int a=200;
		int b=100;
		
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
		

	}

}
